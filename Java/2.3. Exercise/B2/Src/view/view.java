    public void load1(DefaultTableModel res, List<MH> list) {
        res1.getDataVector().removeAllElements();
        jComboBox2.removeAllItems();
        for (MH x: list) {
            res1.addRow(x.toO());
            jComboBox2.addItem(x.getMa()+"-" + x.getTen());
        }
        
    }
    
    public void load2(DefaultTableModel res, List<SinhVien> list) {
        jComboBox3.removeAllItems();
        res2.getDataVector().removeAllElements();
        for (SinhVien x: list) {
            res2.addRow(x.toO());
             jComboBox3.addItem(x.getMa()+"-" + x.getTen());
        }
    }
    
    public void load3(DefaultTableModel res, List<BDK> list) {
        res3.getDataVector().removeAllElements();
        for (BDK x: list) {
            res3.addRow(x.toObject());
        }
    }
    
   
    
