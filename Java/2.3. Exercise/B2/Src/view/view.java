    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        res4.getDataVector().removeAllElements();
        List<Lop> l = new ArrayList<>();
        for (BDK x:dk) {
            boolean check = false;
            for (Lop s:l) {
                if (x.getM().getMa() == s.getM().getMa()) {
                    s.themSv(x.getSv());
                    check = true;
                    break;
                }
            }
            Lop tmp = new Lop(x.getM());
            tmp.themSv(x.getSv());
            if (!check) l.add(tmp);
        }
        for (Lop x:l) {
            res4.addRow(x.toO());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

